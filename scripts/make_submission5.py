def _strip_to(src: str, marker: str) -> str:
    return src[src.index(marker):]


def _rename(body: str, mapping: dict) -> str:
    for a, b in mapping.items():
        body = re.sub(rf"\b{re.escape(a)}\b", b, body)
    return body


def _drop_import_block(body: str, module: str) -> str:
    pat = re.compile(rf"from sb\.{module} import \([^)]*\)\n", re.S)
    return pat.sub("", body)


def _embed_seqs() -> list:
    blobs = []
    for d in SEQ_DIRS:
        d = d.strip()
        path = os.path.join(d, "gru.npz")
        if not os.path.exists(path):
            raise FileNotFoundError(f"missing neural weights: {path}")
        with open(path, "rb") as fh:
            blobs.append(base64.b64encode(fh.read()).decode("ascii"))
        kind = "gru"
        try:
            kd = np.load(path, allow_pickle=True)
            if "kind" in kd.files:
                kind = str(kd["kind"])
        except Exception:
            pass
        print(f"  embedded {kind.upper()} {path} "
              f"({os.path.getsize(path)/1024:.0f} KB)")
    return blobs


def _embed_attns() -> list:
    blobs = []
    for d in ATTN_DIRS:
        d = d.strip()
        path = os.path.join(d, "attn.npz")
        if not os.path.exists(path):
            raise FileNotFoundError(f"missing attention weights: {path}")
        with open(path, "rb") as fh:
            blobs.append(base64.b64encode(fh.read()).decode("ascii"))
        print(f"  embedded ATTN {path} "
              f"({os.path.getsize(path)/1024:.0f} KB)")
    return blobs


