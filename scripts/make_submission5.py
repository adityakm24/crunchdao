    n_members = len(seqs) + (1 if attns else 0)
    w_seq = (1.0 / n_members) if n_members else 0.0

    rank_b, rank_keep = _load_rank()
    rc = np.load(os.path.join(model_directory_path, "rank_const.npz"))
    rk_mu = float(rc["rk_mu"]); rk_sd = float(rc["rk_sd"])
    gbt_mu = float(rc["gbt_mu"]); gbt_sd = float(rc["gbt_sd"])
    rank_row = np.empty((1, len(rank_keep)), dtype=np.float64)

    det = StreamingDetector()

    yield  # signal readiness

    for x_historical, x_online in datasets:
        det.calibrate(np.asarray(x_historical, dtype=np.float64))
        for g in seqs:
            g.reset()
        for a in attns:
            a.reset()
        mu_h = det.mu_h
        sd_h = det.sd_h
        for point in x_online:
            feats = det.update(float(point))
            acc = 0.0
            for b, keep, row in zip(boosters, keeps, rows):
                row[0] = feats[keep]
                p = float(b.predict(row)[0])
                p = min(max(p, 1e-7), 1.0 - 1e-7)
                acc += math.log(p / (1.0 - p)) * w_gbt
            xv = feats[l_keep]
            xs = (xv - l_mean) / l_scale
            np.clip(xs, -8.0, 8.0, out=xs)
            lin_logit = float(np.dot(xs, l_coef)) + l_int
            mean4 = acc / (1.0 - W_LIN)
            rank_row[0] = feats[rank_keep]
            rraw = float(rank_b.predict(rank_row)[0])
            rank_as_gbt = (rraw - rk_mu) / rk_sd * gbt_sd + gbt_mu
            gbt5 = (1.0 - RANK_GW) * mean4 + RANK_GW * rank_as_gbt
            base_logit = (1.0 - W_LIN) * gbt5 + W_LIN * lin_logit
            if seqs or attns:
                seq_logit = 0.0
                for g in calib_seqs:
                    seq_logit += g.step(feats)
                if raw_seqs:
                    rv = _raw_vec(float(point), mu_h, sd_h)
                    for g in raw_seqs:
                        seq_logit += g.step(rv)
                if attns:
                    asum = 0.0
                    for a in attns:
                        asum += a.step(feats)
                    seq_logit += asum / len(attns)
                seq_logit *= w_seq
                final_logit = (1.0 - W_GRU) * base_logit + W_GRU * seq_logit
            else:
                final_logit = base_logit
            score = 1.0 / (1.0 + math.exp(-final_logit))
            if score < 0.0:
                score = 0.0
            elif score > 1.0:
                score = 1.0
            yield score
